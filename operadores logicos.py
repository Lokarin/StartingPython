"""
Operadores : Operacao

AND : Duas condicoes sejam Verdadeiras
OR : Pelo menos uma condicoo seja verdadeira
NOT : Inverte Valor
"""

x = 3
y = 3
z = 4

print(x == y and x == z) #o resultado sera True apenas se as duas informacoes serem verdadeiras
print(x == y or x == z) #o resultado sera True se pelo menos uma das duas informacoes forem verdadeiras
