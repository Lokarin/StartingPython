# -*- coding: utf-8 -*-
"""
== (Igual)
!= (Diferente)
> (Maior que)
< (Menor que)
>= (Maior ou igual que)
<= (Menor ou iguar que)
"""
 
x = 2
y = 3
soma = x + y

print(x == y) #perguntando se sao iguais ou nao
print(x != y) #perguntando se sao diferentes ou nao
print(x > y) #perguntando se x é maior que y
print(x < y) #perguntando se x é menor que y
print(soma)
print(x == soma) #perguntando se x e igual a soma